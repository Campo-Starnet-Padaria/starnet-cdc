    private void update(){
        estatisticas es = new estatisticas();
        estatiscasConsume esc = new estatiscasConsume();
        es = esc.getEstatisticas();
        //Setting on GUI
        this.lblUsers.setText(es.getClientesTotal().toString());
        this.lblUsersAtivos.setText(es.getClientesAtivos().toString());
        this.lblUsersInativos.setText(es.getClientesInativos().toString());
        String valor = es.getLucro().toString();
        valor = "R$ " + valor;
        this.lblLucro.setText(valor);

    }
